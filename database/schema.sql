
CREATE TABLE LOOKUP_VALUES (
  ID NUMBER(10,0) NOT NULL,
  LOOKUP_VALUE VARCHAR2(255) NOT NULL,
  LOOKUP_CD NUMBER(2,0) NOT NULL,
  LOOKUP_GROUP VARCHAR2(20) NOT NULL,
  STATUS_FLG NUMBER(1,0) DEFAULT 1 NOT NULL,
  DESCRIPTION VARCHAR2(255),
  CREATED_BY VARCHAR2(30) NOT NULL,
  CREATED_DATE DATE DEFAULT SYSDATE NOT NULL,
  LAST_UPDATED_BY VARCHAR2(30) NOT NULL,
  LAST_UPDATED_DATE DATE DEFAULT SYSDATE NOT NULL,
  CONSTRAINT PK_LOOKUP_VALUES PRIMARY KEY (ID),
  CONSTRAINT UQ_LOOKUP_GROUP_CD UNIQUE (LOOKUP_GROUP, LOOKUP_CD),
  CONSTRAINT CK_LOOKUP_STATUS CHECK (STATUS_FLG IN (0,1))
);

CREATE SEQUENCE SEQ_LOOKUP_VALUES START WITH 1 INCREMENT BY 1 NOCACHE;

CREATE OR REPLACE TRIGGER TRG_LOOKUP_VALUES_BI
BEFORE INSERT ON LOOKUP_VALUES
FOR EACH ROW
WHEN (NEW.ID IS NULL)
BEGIN
  :NEW.ID := SEQ_LOOKUP_VALUES.NEXTVAL;
  IF :NEW.CREATED_DATE IS NULL THEN
    :NEW.CREATED_DATE := SYSDATE;
  END IF;
  IF :NEW.LAST_UPDATED_DATE IS NULL THEN
    :NEW.LAST_UPDATED_DATE := SYSDATE;
  END IF;
END;
